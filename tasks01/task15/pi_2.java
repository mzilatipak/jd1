package task15;

import static java.lang.Math.pow;
import static java.lang.Math.PI;

public class pi_2 {
	public static void main(String[] args) {
		double pi1, pi2, pi3, pi4;
		pi1 = PI;
		pi2 = pow(PI, 2);
		pi3 = pow(PI, 3);
		pi4 = pow(PI, 4);
		
		System.out.println("pi= " + pi1 + ", pi^2= " +pi2+ ", pi^3= " +pi3 + ", pi^4= "+pi4);
		
		
	}	
}
